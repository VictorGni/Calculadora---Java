    private void digita(String valor){
        
        if(txtResul.getText().equals("0.00")){
            txtResul.setText(valor);
        }
        else{
            txtResul.setText(txtResul.getText().concat(valor));
        }
        
    }
    
    private void limpa (){
        txtResul.setText("0.00");
    }
    
    
    
    

