    private void btnSomaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSomaActionPerformed
       
        if(calController.getValor().equals(txtResul.getText())){
            limpa();
            calController.setOpe("soma");
        }else{
            calController.ralizaOperacoes("soma", Double.parseDouble(txtResul.getText()));
            limpa();
        }

    }//GEN-LAST:event_btnSomaActionPerformed

    private void btnCalculaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculaActionPerformed
      String ope = calController.getOpe();
      
      if(ope.equals("soma")){
        calController.ralizaOperacoes("soma", Double.parseDouble(txtResul.getText()));
        txtResul.setText(calController.getValor());
        
      }
      else if(ope.equals("divi")){
          calController.ralizaOperacoes("divi", Double.parseDouble(txtResul.getText()));
          txtResul.setText(calController.getValor());
          
      }
    }//GEN-LAST:event_btnCalculaActionPerformed

    private void btnDiviActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDiviActionPerformed

        String ope = calController.getValor();
        if(ope.equals(txtResul.getText())){
            limpa();
            calController.setOpe("divi");
        }else{
        calController.ralizaOperacoes("divi", Double.parseDouble(txtResul.getText()));
        limpa();
        }
    }//GEN-LAST:event_btnDiviActionPerformed

